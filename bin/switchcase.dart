void main(){
  String month='feb';
  switch(month){
    case 'jan':
      print('january');
      break;
    case 'feb':
      print('february');
      break;
    case 'mar':
      print('march');
      break;
    case 'apr':
      print('april');
      break;
    case 'may':
      print('may');
      break;
    case 'jun':
      print('june');
      break;
    case 'jul':
      print('july');
      break;
    default:
      print('other');

  }
}